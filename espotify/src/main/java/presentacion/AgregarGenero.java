    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(txtNombreGenero.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor, complete el formulario con el nombre del genero.");
        }else{
            Collection<String> generos_padre = new ArrayList<String>();
            generos_padre.add(txtNombrePadre.getText());
            if(controlGen.crearGeneroConSubgeneros(txtNombreGenero.getText(), generos_padre)){
                JOptionPane.showMessageDialog(null, "Genero agregado con exito.");
                vaciarCampos();
            }else{
                JOptionPane.showMessageDialog(null, "El nombre del genero ya existe, por favor, elija otro.");
                vaciarCampos();
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    public void cargarGeneros() {
        DefaultListModel<String> model = new DefaultListModel();
        Collection<String> retorno = controlGen.mostrarGeneros();
        if (!retorno.isEmpty()) {
            Iterator<String> iterator = retorno.iterator();
            while (iterator.hasNext()) {
                model.addElement(iterator.next());
            }
        }
        jListGeneros.setModel(model);
    }
    
    private void vaciarCampos() {
txtNombreGenero.setText(null);
txtNombrePadre.setText(null);
    }

